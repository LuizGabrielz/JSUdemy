//Mais sobre Numbers

let num1 = 1; //Number
let num2 = 2.5; //Number

console.log(num1.toString() + num2); //Vai realizar a conta
//toString - Vai transformar o número em string
//Quando tem uma string com um operador de + a gente sabe que não realiza a conta.
num1 = num1.toString() // Transformou em String.
console.log(num1.toString(2)); // Representação Binária.
console.log(num1.toFixed(2)); //Casas decimais(geralmente são duas)
console.log(Number.isInteger(num1)); //Essa função retorna se um número é verdadeiro ou não.
